          <p style="text-align:center;">Table 1: AI4WIA Bootcamp Planned Activities</p>
          <p>The bootcamp's journey began with the crucial task of issuing a call for participants, spanning from the 21st to the 28th of August, 2023. This period was dedicated to spreading awareness about the program and inviting individuals interested in enhancing their skills in Artificial Intelligence (AI) and Machine Learning (ML). Through targeted outreach efforts and promotional activities, a comprehensive list of applicants was compiled, setting the stage for the next phase of the program.</p>
          <p>Following the call for participants, the application process closed on the 28th of August, 2023. This marked the deadline for aspiring participants to submit their applications, ensuring that 186 interested individuals had the opportunity to register for the bootcamp. The closure of applications signified the beginning of the selection process, where the organizing team meticulously reviewed each application to shortlist candidates who demonstrated the potential to benefit from the program.</p>
          <p>The shortlisting of successful applicants took place from the 28th to the 29th of August, 2023. During this phase, the organizing team carefully evaluated each application based on predetermined criteria, such as academic background, relevant experience, and motivation. By identifying candidates who met the program's requirements, the shortlisting process ensured that the selected 136 participants were well-suited to engage effectively with the bootcamp's curriculum and activities.</p>
          <p>With the shortlisted candidates identified, the next step involves inviting them to participate in the bootcamp. This involved sending out invitations and facilitating their inclusion in relevant communication channels, such as mailing lists and WhatsApp groups. The invitation process, conducted on the 28th and 29th of August, 2023, aimed to ensure that all selected participants were informed about the program's schedule, expectations, and logistics, fostering a sense of belonging and anticipation among the cohort.</p>
          <p>On the 30th of August, 2023, the bootcamp officially commenced with the 1st introduction meeting with participants. This session served as a platform for participants to acquaint themselves with the program's objectives, activities, and expectations. Additionally, participants were introduced to the pre-assessment evaluation form, which provided insights into their existing knowledge and skills in AI and ML, guiding the customization of the bootcamp experience to meet their needs.</p>
          <p>Continuing the journey, participants were introduced to Coursera on the 30th and 31st of August, 2023. This phase focused on guiding participants through the registration process and facilitating their application for financial aid, ensuring equitable access to the educational resources offered through the platform. By familiarizing participants with Coursera's interface and functionalities, this activity empowered them to leverage online learning opportunities to complement their bootcamp experience.</p>
          <p>The bootcamp's curriculum delved into the fundamentals of AI and ML from the 31st of August to the 2nd of September, 2023. Through a series of interactive sessions, participants received a comprehensive introduction to key concepts, methodologies, and applications in these fields. Accompanied by PowerPoint slides and recorded video lectures, this activity provided participants with a solid foundation for further exploration and engagement in subsequent sessions.</p>
          <p>Expanding on the application of AI and ML, participants were introduced to weather forecasting applications from the 4th to the 5th of September, 2023. Through discussions and demonstrations, participants gained insights into the use of AI and ML algorithms in weather prediction and analysis. Supported by PowerPoint presentations and recorded video tutorials, this activity equipped participants with practical knowledge and skills applicable to real-world scenarios.</p>
          <p>The bootcamp's orientation day, held on the 6th of September, 2023, marked a significant milestone in participants' journey. This event served as an official welcome to the program, providing participants with essential information about the bootcamp's structure, expectations, and support services. Additionally, the orientation day featured the distribution of eFlyers, the creation of attendance lists, and the documentation of the event through recorded videos and photographs, fostering a sense of community and camaraderie among participants.</p>
          <p>As participants progressed through the bootcamp, they were organized into semi-groups on the 7th of September, 2023. This activity aimed to facilitate collaboration, peer learning, and project development among participants with similar interests or objectives. By creating smaller cohorts within the larger cohort, this activity encouraged participants to share insights, resources, and experiences, enriching their learning journey and fostering a supportive learning environment.</p>
          <p>Continuing the exploration of ML, participants engaged in further discussions and project work from the 7th to the 9th of September, 2023. Through hands-on activities and assignments, participants applied their knowledge and skills to solve real-world problems, culminating in the completion and submission of assigned projects. This activity provided participants with an opportunity to demonstrate their understanding and proficiency in ML concepts and methodologies, preparing them for the challenges ahead.</p>
          <p>As participants embarked on their learning journey through Coursera courses from the 11th of September to the 22nd of December, 2023, they were tasked with documenting their progress and achievements. Through weekly progress reports and the uploading of certificates of proficiency to Drive, participants tracked their learning outcomes and accomplishments, reflecting on their growth and development in AI and ML.</p>
          <p>The bootcamp's highlight, the hackathon, kicked off on the 11th of September, 2023, marking a culmination of participants' learning and collaboration. During this intense period of group work and project development, participants applied their knowledge and skills to tackle real-world challenges in AI and ML, demonstrating their creativity, problem-solving abilities, and teamwork. Through the hackathon, participants gained practical experience and insights into the application of AI and ML in diverse domains, preparing them for future endeavors.</p>
          <p>Following the completion of the hackathon, participants submitted their projects for evaluation from the 22nd to the 24th of December, 2023. This phase involved the meticulous review and assessment of project submissions, considering criteria such as innovation, feasibility, and impact. By showcasing their work and ideas, participants demonstrated their mastery of AI and ML concepts and their ability to address complex problems effectively.</p>
          <p>The virtual presentation of projects to a panel of judges on the 7th and 9th of January, 2023, provided participants with an opportunity to showcase their projects and receive feedback from experts in the field. Through engaging presentations and interactive discussions, participants demonstrated their understanding of project objectives, methodologies, and outcomes, highlighting their achievements and insights gained during the bootcamp.</p>
          <p>The final panel meeting on the 13th of January, 2024, marked the culmination of the evaluation process, where judges deliberated on project submissions and finalized decisions. This meeting provided an opportunity for judges to review project reports, assess presentations, and select the top three projects based on predetermined criteria. By recognizing outstanding achievements and contributions, the panel meeting celebrated participants' efforts and showcased the diversity and impact of projects developed during the bootcamp.</p>
          <p>The journey concluded with a closure and award ceremony on the 21st of January, 2024, celebrating participants' accomplishments and recognizing their dedication and hard work throughout the bootcamp. During this event, participants received certificates of completion and awards for outstanding performance, acknowledging their achievements and contributions to the program. The closure and award ceremony served as a testament to the success of the bootcamp in fostering talent, innovation, and collaboration in the field of AI and ML, inspiring participants to continue their journey of learning and growth.</p>
          
