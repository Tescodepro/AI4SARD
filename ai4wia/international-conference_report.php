          <h3 class="title">International conference Report</h3>
          <h4>Summary of Report</h4>
        
          <p> The Artificial Intelligence for Sustainable Agriculture and Rural Development (AI4SARD) research team
            funded by the of Gender and Responsible Artificial Intelligence Network (GRAIN) organised the 1st
            international hybrid conference on Artificial Intelligence for Women in Agriculture (AI4WIA) at the Kwara
            State University, Malete, Kwara State between 23rd to 26th October, 2023. The theme of the conference was
            <b>AI-Enhanced Climate-Smart Farming: Towards Empowering African Women Farmers”</b>. The purpose of the
            conference
            was to develop co-created recommendations from the space of academia, non-governmental Organisation and
            researchers across the globe on the theme. With over one hundred attendees, the conference availed the
            presentations of a key-note paper, 5 lead papers and 7 technical papers. </b>
          </p>
         
          <p style="text-align:center;">AI4WIA international conference Planned Activities</p>
          <p>The conference was declared open by the Acting Vice-Chancellor, Kwara State University, Malete, Prof.
            Shaykh Lukman Jimoh. He termed this conference as timely and imperative due to the significant role played
            by agriculture in the world economies. He added that the need to revolutionize agriculture for economic
            sustainability of any nation is paramount most especially at this current critical time when the world is
            facing unprecedented challenges in food security and agriculture. He further stressed that this conference
            holds a transformative change by bringing together experts, academics, and stakeholders from diverse
            disciplines, leveraging on the power of artificial intelligence and climate-smart farming to drive the
            progress in our agricultural sector. The <b>principal investigator of the project, Associate Professor,
              Olayinka Jelili Yusuf</b>, expressed his delight by welcoming everyone while noting that people gather in
            hybrid
            mode, connecting virtually and physically to explore and collaborate. He reflects the core values of the
            team’s project and the driving force behind the collective mission. He also mentioned that the team is
            harnessing the power of cutting-edge AI technologies to address the unique challenges faced by women in
            agriculture across the African continent. </p>
          <p>The key note presenter, <b>Prof. Olanike Deji</b> from the Department of Agricultural Extension and Rural
            Development, Obafemi Awolowo University, Osun State, Nigeria gave a complete address of “AI-Enhanced Climate
            Smart Farming: Towards empowering African Women Farmers” by stating that women farmers play significant and
            multifaceted roles in Agriculture covering food production, livestock, community development, water
            management, income generation, nutrition and household well-being, etc. She also emphasized the reason why
            this topic was targeted at women farmers being that, they are tend to adapt and adopt several climate change
            resilience. However, She noted several challenges women farmers encounter in the course of engaging in
            farming activities in Africa such as limited access to land and resources, inadequate training and extension
            services, gender-based violence, climate change vulnerability, illiteracy and lack of information inadequate
            representation, etc. All these impedes women in contributing fully to Agriculture. She further presented
            some efforts put in place to address these challenges as gender-sensitive agricultural policies, access to
            credit, education and literacy, Agricultural Extension Services, empowerment and capacity building,
            awareness and advocacy, rural infrastructure development, climate smart Agriculture, among others. She
            expressed the nature of agricultural system practiced in Africa as being rain fed, this has however affected
            women farmers due to erratic weather patterns as a result of climate change. Thus, resulted to women
            farmers’ water scarcity, reduced crop yields, food insecurity, increased workload livestock health and
            displacement and migration. This climate change has caused great impacts along Agricultural Value chain,
            hence the need for climate smart farming cannot be overemphasized. She also highlighted the importance of
            gender inclusive design, local language and literacy, training and capacity building, community engagement,
            financial inclusion etc as gender-responsive strategies in integrating AI- enhanced Climate Smart Farming
            into African Agriculture. These AI-enhanced Climate Smart Farming in Africa can cut across AI-Enhanced
            weather forecasting, crop monitoring and Disease detection, Precision Agriculture, Digital Advisory
            Services, Smart Irrigation systems, Livestock Management, Climate resilient varieties, drones for Farming,
            remote sensing for climate data etc. In order to ensure, productivity, sustainability, and resilience in the
            face of climate change, It is therefore imperative to continue developing and adopting these technologies
            using socio-technical methodology that could produce responsible AI-Enhanced Climate Smart technologies that
            are accessible and beneficial to all farmers especially small holders and marginalized women farmers in
            Africa.</p> 
           <p>The first lead presenter, <b>Dr. Abdulwaheed Musa</b>, the Director, Center for Artificial
            Intelligence and
            Machine Learning System (CAMS), Kwara State University, Malete unlock the application of Artificial
            Intelligence Innovation in Predictive Agriculture by stating predictive agriculture as data analytics and
            technology which anticipate crop outcomes and improve farming strategies, enhancing productivity and
            sustainability. Also, the importance of AI in Agriculture as being able to harness advanced technology to
            automate and improve farming operations leading to increased productivity, reduced resource wastage and
            enhance decision-making for sustainable food production, <b>Dr. Abdulwaheed </b>said. He further expressed
            that,
            over the years, there has been evolution of technology such as machines and robotics which will be
            controlled by automation systems equipped with machine learning algorithms invariably, leading to minimal
            inputs from human operators in coming years. In the same vein, there has been series of evolution in
            Agricultural sector, from the ancient time to the fourth industrial revolution and AI innovations. The AI
            tools are capable of addressing predicting analysis, predicting crop yields, application of drone, detecting
            pests and diseases, livestock health monitoring, improved soil health monitoring, precision agriculture and
            automatic weeding. He further noted that, predictive agriculture is crucial and capable of addressing
            climate change and its impacts, food security and sustainable agriculture as well as the food for precision
            farming. <b>Dr. Abdulwaheed</b> noted some benefits of AI as increased crop productivity, resource
            efficiency,
            reduced environmental impacts and data driven decision-making. He however, stated the challenges and future
            prospects as data privacy and security, accessibility and adoption, policy and regulation and research and
            development. He ended his presentation with emphasis on empowering African Women Farmers through AI-powered
            educational initiatives/programs to provide guidance with knowledge on sustainable farming practices. Also,
            AI-enabled resource accessibility that can facilitate efficient resource allocation, by ensuring women
            farmers have access to essential resources, and mitigate the impacts of climate change on their agricultural
            endeavours. </p>
          <p>In the presentation done by the second presenter, <b>Dr. Chinwoke Clara Ifeanyi-obi</b>, Department of
            Agricultural Extension and Development Studies, University of Portharcourt, Rivers State, Nigeria, She
            defined climate change as significant variations in the mean in the mean state of climate lasting for an
            extended period of time like a decade or more, caused by all activities that contribute to emission of
            greenhouse gases particularly carbon dioxide (CO2), Methane, nitrous oxide, etc. into the atmosphere,
            majorly classified as human and natural causes. Importantly, she noted Agriculture as the single most
            important economic activity in Africa that provides employment for about two-thirds of the continent´s
            working population and for some countries in African, contributes an average of 30 to 60 percent of the
            value of exports. She further noted Agriculture as the worst hit sector by climate change and Nigeria being
            recognized as vulnerable to climate change impacts, ranked 160 out of 181 countries. Hence, need for climate
            smart Agriculture to achieve increased productivity, enhanced resilience and reduced emissions in order to
            maintain ecosystem services. She opined climate change as not being neutral because it impacts men and women
            differently. She further expressed gender-responsive as means of addressing the different situations, roles,
            needs and interests of women, men, girls, boys, people living with special needs in the design and
            implementation of activities, policies and programs. However, the challenges to uptake of climate smart
            Agriculture among women farmers were highlighted as poor knowledge, capacity and income earning power, poor
            access to information and resources, low involvement and participation in climate change, low implementation
            of gender commitments in existing policies. Hence, knowledge and capacity building must be demand and
            need-driven and done at frequent intervals, structured approach for sustained access to information and
            needed resources among women, women participation in climate interventions must go beyond consultation to
            achieve involvement, operationalization and implementation of existing policies are crucial.</p>
          <p>``The changing paradigms in addressing climate shock among women farmers`` addressed by the third lead
            paper presenter, <b>Dr. Sidiqat Aderinoye-Abdulwahab</b>, of the Department of Agricultural Extension and
            Rural
            Development, University of Ilorin, was described as being apt due to the havoc caused by climate change and
            thereby revealed the ways of changing paradigms by listening to women, increasing initiative that support
            womens´ growth, reduce womens´ workloads, breakdown the barriers holding rural women back such as access,
            equity and disability. These can be achieved through carbon trading, use of alternative energy, adopting
            gender climate smart agriculture, etc, with efforts to collectively boost women farmers resilience to
            climate change and decision-making capacities, policy design that will boost capacity to listen to women
            should be implemented, improvement in agricultural extension services and training programs to better serve
            women farmers.</p>
          <p>The lessons from the SOFADRICLIMATE app in Nigeria as AI systems for climate change predictions and
            resilience, delivered by <b>Dr. Toyin Samuel Olowogbon</b>, from Centre for sustainable Agricultural
            Empowerment,
            Nigeria revealed approach used in developing the app such as non-technical data driven climate information,
            cost effective climate information for annual decision support, novel and robust contents on best agronomic
            practices on maize and weather index micro insurance for farmers, while lessons from the process were also
            stated as poor digital literacy as third party on board some users, developing a web-based app for farmers
            may not be sufficient 94% of users wanted monthly message reminder, high preference for web-based app
            compared to the SMS option based on usage, convenience and cost. He further suggested next actions for the
            app as integration of flood warning reports and token annual subscription to on board international farmers.
          </p>
          <p>The last paper presented on ``Artificial Intelligence – driven weed Management and sustainable Farming``
            <b>by
              Engineer Jibril Abdullahi Bala</b> from the Department of Mechatronics Engineering, Federal University of
            Technology, Minna showed the various AI in weed detection as deep learning algorithms, transfer learning,
            reinforcement learning, object detection models, optimization algorithms and image segmentation techniques.
            He further concluded that, the integration of AI in Agriculture, specifically in weed management, has shown
            the potential to revolutionize traditional farming practices, and as we envision the future of agriculture
            in Nigeria, this blend of technology, sustainability and inclusivity sets the stage for a promising future
            in Nigerian Agriculture.
          </p>

